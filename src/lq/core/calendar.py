def _is_holiday(d: date) -> bool:
    """判断是否为法定节假日（基于内置列表）。"""
    year_holidays = _KNOWN_HOLIDAYS.get(d.year, [])
    return (d.month, d.day) in year_holidays


def is_trading_day(d: date) -> bool:
    """判断是否为 A 股交易日。

    规则：非周末 + 非法定节假日。
    """
    if d.weekday() >= 5:  # 周六=5，周日=6
        return False
    if _is_holiday(d):
        return False
    return True


def next_trading_day(d: date) -> date:
    """返回 d 之后的第一个 A 股交易日（不含 d 本身）。

    用于 T+1 语义：signal_date=T，entry_date=next_trading_day(T)。
    """
    candidate = d + timedelta(days=1)
    # 最多向后找 14 天（覆盖最长连续假期）
    for _ in range(14):
        if is_trading_day(candidate):
            return candidate
        candidate += timedelta(days=1)
    # 理论上不会走到这里，保底 fallback
    return candidate
